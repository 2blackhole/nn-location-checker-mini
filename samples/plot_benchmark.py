#!/usr/bin/env python3
"""
Plot benchmark results from a CSV file generated by benchmark_models.py.
"""

import argparse
import csv
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np


def create_argparser() -> argparse.ArgumentParser:
    """Create argument parser for plotting script."""
    parser = argparse.ArgumentParser(
        description="Plot benchmark results from CSV file"
    )
    parser.add_argument(
        "-i", "--input", type=Path, default=Path("benchmark_results.csv"),
        help="Input CSV file (default: benchmark_results.csv)"
    )
    parser.add_argument(
        "-o", "--output", type=Path, default=Path("benchmark_plots.png"),
        help="Output plot file (default: benchmark_plots.png)"
    )
    parser.add_argument(
        "--show", action="store_true",
        help="Show plot interactively (default: save only)"
    )
    return parser


def load_data(csv_path: Path) -> tuple[list[str], list[float], list[float], list[float]]:
    """Load CSV and return labels, accuracy, macro_f1, avg_time_per_image.

    Returns:
        Tuple of (labels, accuracy_list, macro_f1_list, avg_time_list)
    """
    labels: list[str] = []
    accuracy: list[float] = []
    macro_f1: list[float] = []
    avg_time: list[float] = []

    with csv_path.open("r", encoding="utf-8") as f:
        reader = csv.DictReader(f)

        # Check if classifier_name column exists
        has_classifier_name = "classifier_name" in reader.fieldnames if reader.fieldnames else False

        for row in reader:
            donor = row.get("donor", "").strip()
            segment = row.get("segment", "").strip()
            classifier_name = row.get("classifier_name", "").strip() if has_classifier_name else ""

            # Skip rows with missing essential data
            if not donor:
                continue
            try:
                acc = float(row["accuracy"])
                f1 = float(row["macro_f1"])
                avg = float(row["avg_time_per_image"])
            except (KeyError, ValueError, TypeError):
                continue

            # Create label
            if has_classifier_name and classifier_name:
                label = f"{donor} : {classifier_name}"
            else:
                label = f"{donor} ({segment})" if segment else donor

            labels.append(label)
            accuracy.append(acc)
            macro_f1.append(f1)
            avg_time.append(avg)

    return labels, accuracy, macro_f1, avg_time


def plot_metrics(
    labels: list[str],
    accuracy: list[float],
    macro_f1: list[float],
    avg_time: list[float],
    output_path: Path,
    show: bool = False,
) -> None:
    """Generate 2x2 subplot with accuracy, macro F1, time per image, and FPS.

    Adds value labels on top of each bar.
    """
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle("Model Benchmark Comparison", fontsize=16)

    x = np.arange(len(labels))
    width = 0.6

    # Accuracy
    ax = axes[0, 0]
    bars_acc = ax.bar(x, accuracy, width, color="skyblue")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Accuracy")
    ax.set_title("Test Accuracy")
    ax.set_ylim(0, max(1.0, max(accuracy) * 1.1))
    ax.bar_label(bars_acc, fmt="%.1f%%", label_type="edge", padding=2)

    # Macro F1
    ax = axes[0, 1]
    bars_f1 = ax.bar(x, macro_f1, width, color="lightgreen")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Macro F1 Score")
    ax.set_title("Macro-averaged F1")
    ax.set_ylim(0, max(1.0, max(macro_f1) * 1.1))
    ax.bar_label(bars_f1, fmt="%.1f%%", label_type="edge", padding=2)

    # Average time per image (seconds)
    ax = axes[1, 0]
    bars_time = ax.bar(x, avg_time, width, color="salmon")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Seconds")
    ax.set_title("Average Time per Image")
    if max(avg_time) / min(avg_time) > 100:
        ax.set_yscale("log")
        for bar, val in zip(bars_time, avg_time, strict=False):
            ax.text(bar.get_x() + bar.get_width()/2, val, f"{val:.3f}s",
                    ha="center", va="bottom", fontsize=8)
    else:
        ax.set_ylim(0, max(avg_time) * 1.1)
        ax.bar_label(bars_time, fmt="%.3fs", label_type="edge", padding=2)

    # FPS (images per second) - recomputed from avg_time
    fps = [1.0 / t for t in avg_time]
    ax = axes[1, 1]
    bars_fps = ax.bar(x, fps, width, color="gold")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Images/sec")
    ax.set_title("Throughput (FPS)")
    if max(fps) / min(fps) > 100:
        ax.set_yscale("log")
        for bar, val in zip(bars_fps, fps, strict=False):
            label = f"{val:.1f}" if val >= 10 else f"{val:.2f}"
            ax.text(bar.get_x() + bar.get_width()/2, val, label,
                    ha="center", va="bottom", fontsize=8)
    else:
        ax.set_ylim(0, max(fps) * 1.1)

        def fmt_fps(x: float) -> str:
            """Format FPS value: integer if >=1 else 2 decimals."""
            return f"{x:.0f}" if x >= 1 else f"{x:.2f}"

        ax.bar_label(bars_fps, fmt=fmt_fps, label_type="edge", padding=2)

    plt.tight_layout()
    plt.savefig(output_path, bbox_inches="tight", dpi=150)
    if show:
        plt.show()
    else:
        plt.close()
    print(f"Plots saved to {output_path}")


def main() -> None:
    """Main entry point."""
    args = create_argparser().parse_args()
    if not args.input.exists():
        print(f"Error: Input file {args.input} not found.")
        return

    labels, accuracy, macro_f1, avg_time = load_data(args.input)
    if not labels:
        print("No valid data rows found in CSV.")
        return

    plot_metrics(labels, accuracy, macro_f1, avg_time, args.output, args.show)


if __name__ == "__main__":
    main()